import { Injectable } from '@angular/core';
import { BridgeSketchModel } from '../classes/bridge-sketch.model';
import { DesignConditions } from './design-conditions.service';
import { Point2D } from '../classes/graphics';

@Injectable({ providedIn: 'root' })
export class BridgeSketchDataService {

  public addSketchesFromDataForConditions(models: BridgeSketchModel[], designConditions: DesignConditions) {
    const raw_data = RAW_DATA[designConditions.tagGeometryOnly];
    if (!raw_data) {
      return;
    }
    raw_data.forEach(({ name, text }) =>
      models.push(this.populateFromText(new BridgeSketchModel(name, designConditions), text)),
    );
  }

  private populateFromText(sketch: BridgeSketchModel, text: string): BridgeSketchModel {
    const tokens = text.split('|');
    let i: number = 0;
    const geometryTag = tokens[i++];
    if (geometryTag !== sketch.designConditions.tagGeometryOnly) {
      throw new Error(`Tag mismatch: ${geometryTag} vs ${sketch.designConditions.tagGeometryOnly}`);
    }
    const jointCount = parseInt(tokens[i++]);
    const memberCount = parseInt(tokens[i++]);
    for (let j: number = 0; j < jointCount; ++j) {
      const x = parseFloat(tokens[i++]);
      const y = parseFloat(tokens[i++]);
      sketch.joints.push(new Point2D(x, y));
    }
    for (let j: number = 0; j < memberCount; ++j) {
      const ia = parseInt(tokens[i++]) - 1;
      const ib = parseInt(tokens[i++]) - 1;
      sketch.members.push({ a: sketch.joints[ia], b: sketch.joints[ib] });
    }
    return sketch;
  }
}

